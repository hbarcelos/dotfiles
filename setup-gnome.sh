#!/usr/bin/env bash

set -euo pipefail

if ! command -v gsettings >/dev/null 2>&1; then
  echo "error: gsettings is not installed or not in PATH" >&2
  exit 1
fi

if ! command -v xdg-mime >/dev/null 2>&1; then
  echo "error: xdg-mime is not installed or not in PATH" >&2
  exit 1
fi

SKIP_BACKUP=false
for arg in "$@"; do
  case "$arg" in
    --no-backup)
      SKIP_BACKUP=true
      ;;
    -h|--help)
      echo "Usage: $0 [--no-backup]"
      echo "  --no-backup   Apply settings without saving a backup snapshot"
      exit 0
      ;;
    *)
      echo "error: unknown option: $arg" >&2
      echo "Try: $0 --help" >&2
      exit 1
      ;;
  esac
done

set_key() {
  local schema="$1"
  local key="$2"
  local value="$3"
  gsettings set "$schema" "$key" "$value"
}

get_key() {
  local schema="$1"
  local key="$2"
  gsettings get "$schema" "$key"
}

save_current_settings() {
  local ts backup_dir backup_file
  ts="$(date +%Y%m%d-%H%M%S)"
  backup_dir="$HOME/.local/state/gnome-setup"
  backup_file="${backup_dir}/backup-${ts}.sh"
  mkdir -p "$backup_dir"

  {
    echo "#!/usr/bin/env bash"
    echo "set -euo pipefail"
    echo
    echo "# Generated by setup-gnome.sh on ${ts}"
    echo "gsettings set org.gnome.desktop.interface gtk-theme $(get_key org.gnome.desktop.interface gtk-theme)"
    echo "gsettings set org.gnome.desktop.interface icon-theme $(get_key org.gnome.desktop.interface icon-theme)"
    echo "gsettings set org.gnome.desktop.interface color-scheme $(get_key org.gnome.desktop.interface color-scheme)"
    echo
    echo "gsettings set org.gnome.shell.extensions.window-list display-all-workspaces $(get_key org.gnome.shell.extensions.window-list display-all-workspaces)"
    echo "gsettings set org.gnome.shell.extensions.window-list embed-previews $(get_key org.gnome.shell.extensions.window-list embed-previews)"
    echo "gsettings set org.gnome.shell.extensions.window-list grouping-mode $(get_key org.gnome.shell.extensions.window-list grouping-mode)"
    echo "gsettings set org.gnome.shell.extensions.window-list show-on-all-monitors $(get_key org.gnome.shell.extensions.window-list show-on-all-monitors)"
    echo
    echo "gsettings set org.gnome.desktop.wm.keybindings switch-to-workspace-1 $(get_key org.gnome.desktop.wm.keybindings switch-to-workspace-1)"
    echo "gsettings set org.gnome.desktop.wm.keybindings switch-to-workspace-2 $(get_key org.gnome.desktop.wm.keybindings switch-to-workspace-2)"
    echo "gsettings set org.gnome.desktop.wm.keybindings switch-to-workspace-3 $(get_key org.gnome.desktop.wm.keybindings switch-to-workspace-3)"
    echo "gsettings set org.gnome.desktop.wm.keybindings switch-to-workspace-4 $(get_key org.gnome.desktop.wm.keybindings switch-to-workspace-4)"
    echo "gsettings set org.gnome.desktop.wm.keybindings move-to-workspace-1 $(get_key org.gnome.desktop.wm.keybindings move-to-workspace-1)"
    echo "gsettings set org.gnome.desktop.wm.keybindings move-to-workspace-2 $(get_key org.gnome.desktop.wm.keybindings move-to-workspace-2)"
    echo "gsettings set org.gnome.desktop.wm.keybindings move-to-workspace-3 $(get_key org.gnome.desktop.wm.keybindings move-to-workspace-3)"
    echo "gsettings set org.gnome.desktop.wm.keybindings move-to-workspace-4 $(get_key org.gnome.desktop.wm.keybindings move-to-workspace-4)"
    echo "gsettings set org.gnome.desktop.wm.keybindings switch-windows $(get_key org.gnome.desktop.wm.keybindings switch-windows)"
    echo "gsettings set org.gnome.desktop.wm.keybindings switch-applications $(get_key org.gnome.desktop.wm.keybindings switch-applications)"
    echo "gsettings set org.gnome.desktop.wm.keybindings show-desktop $(get_key org.gnome.desktop.wm.keybindings show-desktop)"
    echo
    echo "xdg-mime default $(xdg-mime query default inode/directory) inode/directory"
    echo "xdg-mime default $(xdg-mime query default application/x-gnome-saved-search) application/x-gnome-saved-search"
    echo
    if gsettings list-schemas | grep -qx "org.gnome.shell.extensions.dash-to-panel"; then
      gsettings list-recursively org.gnome.shell.extensions.dash-to-panel 2>/dev/null \
        | sed -E 's#^org\\.gnome\\.shell\\.extensions\\.dash-to-panel[[:space:]]+([^[:space:]]+)[[:space:]]+#\\1=#' \
        | while IFS='=' read -r key value; do
            [[ -n "$key" ]] && echo "gsettings set org.gnome.shell.extensions.dash-to-panel ${key} ${value}"
          done
    fi
  } > "$backup_file"

  chmod +x "$backup_file"
  echo "Saved current settings snapshot to: $backup_file"
}

