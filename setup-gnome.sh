set_key org.gnome.desktop.wm.keybindings switch-applications "@as []"
set_key org.gnome.desktop.wm.keybindings show-desktop "['<Super>d']"

# Remove GNOME's default Super+1..9 app shortcuts so they do not
# conflict with workspace switching.
for i in {1..9}; do
  set_key org.gnome.shell.keybindings "switch-to-application-${i}" "@as []"
done

# Custom launchers.
BASE="/org/gnome/settings-daemon/plugins/media-keys/custom-keybindings"
K0="${BASE}/custom0/"
K1="${BASE}/custom1/"
K2="${BASE}/custom2/"
TERMINAL_LAUNCHER="bash -lc \"term=\$(gsettings get org.gnome.desktop.default-applications.terminal exec | tr -d \\\"\\\\047\\\"); if [ -n \\\"\$term\\\" ] && command -v \\\"\$term\\\" >/dev/null 2>&1; then exec \\\"\$term\\\"; elif command -v kitty >/dev/null 2>&1; then exec kitty; elif command -v gnome-terminal >/dev/null 2>&1; then exec gnome-terminal; else exec xterm; fi\""
set_key org.gnome.settings-daemon.plugins.media-keys custom-keybindings "['${K0}', '${K1}', '${K2}']"
set_custom_keybinding "$K0" "Terminal" "$TERMINAL_LAUNCHER" "<Super>Return"
set_custom_keybinding "$K1" "Files" "nautilus --new-window" "<Super>e"
set_custom_keybinding "$K2" "Terminal (Ctrl+Alt+T)" "$TERMINAL_LAUNCHER" "<Primary><Alt>t"

# Window List extension behavior.
set_key org.gnome.shell.extensions.window-list display-all-workspaces "false"
set_key org.gnome.shell.extensions.window-list embed-previews "true"
set_key org.gnome.shell.extensions.window-list grouping-mode "'never'"
