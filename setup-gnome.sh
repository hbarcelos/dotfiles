set_key org.gnome.desktop.interface icon-theme "'Papirus-Dark'"
set_key org.gnome.desktop.interface color-scheme "'prefer-dark'"

# Workspace navigation and moving windows.
set_key org.gnome.desktop.wm.keybindings switch-to-workspace-1 "['<Super>1']"
set_key org.gnome.desktop.wm.keybindings switch-to-workspace-2 "['<Super>2']"
set_key org.gnome.desktop.wm.keybindings switch-to-workspace-3 "['<Super>3']"
set_key org.gnome.desktop.wm.keybindings switch-to-workspace-4 "['<Super>4']"

set_key org.gnome.desktop.wm.keybindings move-to-workspace-1 "['<Shift><Super>1']"
set_key org.gnome.desktop.wm.keybindings move-to-workspace-2 "['<Shift><Super>2']"
set_key org.gnome.desktop.wm.keybindings move-to-workspace-3 "['<Shift><Super>3']"
set_key org.gnome.desktop.wm.keybindings move-to-workspace-4 "['<Shift><Super>4']"

# Alt+Tab switches windows directly.
