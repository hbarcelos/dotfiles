set_custom_keybinding() {
  local path="$1"
  local name="$2"
  local command="$3"
  local binding="$4"
  local schema="org.gnome.settings-daemon.plugins.media-keys.custom-keybinding:${path}"

  set_key "$schema" name "'${name}'"
  set_key "$schema" command "'${command}'"
  set_key "$schema" binding "'${binding}'"
}

